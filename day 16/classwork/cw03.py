# 4) ერთიდან 100 მდე დაბეჭდეთ ყველა რიცხვი თან გვერძე მიუწერეთ ლუწია თუ კენტი  while loop ის გამოყენებით 


firs_number = 1

while firs_number != 100:
    firs_number += 1
    if firs_number % 2 == 0 :
        print(f"{firs_number}. is even")

    else:
        print(f"{firs_number}. is even")










